    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String input = jTextField1.getText();
        try {
            double side = Double.parseDouble(input.trim());

            double perimeter = 4 * side;
            double area = side * side;

            jTextField2.setText(String.valueOf(perimeter));
            jTextField3.setText(String.valueOf(area));


            try (FileWriter writer = new FileWriter("ket_qua.txt", true)) {
                writer.write("Chu vi: " + perimeter + "\n");
                writer.write("Diện tích: " + area + "\n");
                JOptionPane.showMessageDialog(this, "Kết quả đã được lưu vào file ket_qua.txt");
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Lỗi khi lưu kết quả vào file");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng cạnh hình vuông");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

